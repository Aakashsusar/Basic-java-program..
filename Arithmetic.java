public class Arithmetic {
    public static void main(String[] args) {
        int a, b;
        a = 34;
        b = 12;

        System.out.println("a =" + a);
        System.out.println("b =" + b);

        a += b;
        System.out.println(a);

        a *= b;
        System.out.println(a);

        a -= b;
        System.out.println(a);

        a /= b;
        System.out.println(a);

        a %= b;
        System.out.println(a);

    }

}
