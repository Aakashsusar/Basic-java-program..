public class Bitwise {
    public static void main(String[] args) {
        int a = 9;
        int b = 6;
        int result = a & b;
        int x = a | b;
        int y = a ^ b;
        int p = a >> b;
        int q = a << b;
        int r = a >>> b;
        System.out.println(result);
        System.out.println(x);
        System.out.println(y);
        System.out.println(p);
        System.out.println(q);
        System.out.println(r);

    }

}
